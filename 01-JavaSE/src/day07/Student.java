package day07;

// 定义一个类，类名为student
// Student是一个类，代表了所有的学生对象，是一个学生模板。
public class Student{ // 定义一个公开的类，起名为Student

	// 属性【描述的是对象的状态信息】
	// 顺序通常采用变量的方式来定义
	// 在类体中，方法体之外的变量成为“成员变量”
	// 成员变量没有赋值，系统赋默认值：一切向0看齐

	// 学号
	int no;
	// 姓名
	String name;
	// 性别
	boolean sex;
	// 年龄
	int age;
	// 住址
	String address;

	// 方法
	// 方法描述的是对象的动作信息
	// 当前例子就只描述属性了，不描述方法
}
